String getbg2(String country) {
  switch (country) {
    case 'USD':
      return 'assets/images/usd2.jpeg';
    case 'EUR':
      return 'assets/images/eur2.jpeg';
    case 'GBP':
      return 'assets/images/gbp2.jpeg';
    case 'AED':
      return 'assets/images/aed2.jpeg';
    case 'SAR':
      return 'assets/images/sar2.jpeg';
    case 'CAD':
      return 'assets/images/cad2.jpeg';
    case 'AUD':
      return 'assets/images/aud2.jpeg';
    case 'SGD':
      return 'assets/images/sgd2.jpeg';
    case 'JPY':
      return 'assets/images/jpy2.jpeg';
    case 'CNY':
      return 'assets/images/cny2.jpeg';
    default:
      return 'assets/images/usd2.jpeg';
  }
}
